-- 目的      : アプリ用ロールとデータベース(fullness-ecx)を作成する。
-- 注意      : データベース名にハイフンを含むため、SQL 上では必ず
--             ダブルクォート("fullness-ecx")で囲みます。
--             ※ Flyway ではデータベース自体は作成できないため、
--               この 00 番のスクリプトのみ手動実行が必要です。
-- =====================================================================

-- ---------------------------------------------------------------------
-- 1. データベースの作成
--    ENCODING は UTF8。TEMPLATE template0 を用いることで
--    エンコーディング不整合エラーを避けます。
--    OWNER をアプリ用ロールにすることで、以降の DDL/DML を
-- ---------------------------------------------------------------------
CREATE DATABASE "fullness-ecx"
    WITH OWNER = postgres
         ENCODING = 'UTF8'
         TEMPLATE = template0;

-- 接続確認用コメント:
--   psql -U fullness -h localhost -d "fullness-ecx"
